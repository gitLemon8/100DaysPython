# Functions without input
def greet():
    print("Hello Angela")
    print("How do you do Angela?")
    print("Okay have a great day!")

greet()

# Functions that allows for inputs
def greet_with_name(name):
    print(f"Hello {name}")
    print(f"How do you do {name}?")
    print("Okay have a great day")

greet_with_name("Angela")